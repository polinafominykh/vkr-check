from vkrguard.llm.client import ask_llm


def check_introduction(introduction_text: str) -> str:
    prompt = f"""
Проанализируй раздел "Введение" выпускной квалификационной работы.

Определи, есть ли в тексте:
1. актуальность
2. цель исследования
3. задачи исследования
4. объект исследования
5. предмет исследования
6. методы исследования

Ответ дай строго в формате:

Актуальность: да/нет
Цель: да/нет
Задачи: да/нет
Объект: да/нет
Предмет: да/нет
Методы: да/нет
Комментарий: ...

Текст введения:
{introduction_text[:8000]}
"""
    return ask_llm(prompt)